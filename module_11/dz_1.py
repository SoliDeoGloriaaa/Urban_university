import numpy

arr = numpy.array([1, 2, 3, 4, 5])

print("Исходный массив:")
print(arr)

print("Сумма всех элементов в массиве:", numpy.sum(arr))

print("Результат умножения на 2:")
print(arr * 2)

print("Результат возведения в квадрат:")
print(arr ** 2)

print("Среднее значение массива:", numpy.mean(arr))

print("Максимальный элемент в массиве:", numpy.max(arr))

print("Минимальный элемент в массиве:", numpy.min(arr))
